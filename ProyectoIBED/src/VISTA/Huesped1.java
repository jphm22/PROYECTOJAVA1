    private void btnGuardarHActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarHActionPerformed
        
        try {
            PreparedStatement guardar =  (PreparedStatement) conect.prepareStatement("INSERT INTO huesped (nombre,apellido,dni,edad) VALUE (?,?,?,?)");
            guardar.setString(1,txtNombre.getText());
            guardar.setString(2,txtApellido.getText());
            guardar.setString(3,txtDni.getText());
            guardar.setString(4,txtEdad.getText());
            
            this.modelo.addRow(new Object[]{this.txtNombre.getText(),this.txtApellido.getText(),this.txtDni.getText(),this.txtEdad.getText()});
            this.txtNombre.setText("");
            this.txtApellido.setText("");
            this.txtDni.setText("");
            this.txtEdad.setText("");
            txtNombre.grabFocus();
            
            guardar.executeUpdate();
            JOptionPane.showMessageDialog(null, "Huespedu registrado");
            
            // Limpiar los campos
            txtNombre.setText("");
            txtApellido.setText("");
            txtDni.setText("");           
            txtEdad.setText("");
            txtNombre.requestFocus();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e + " No se logro registrar el huesped");
        }
        
        
    }//GEN-LAST:event_btnGuardarHActionPerformed

