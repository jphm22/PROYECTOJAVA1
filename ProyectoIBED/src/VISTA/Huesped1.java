    public void eliminarHuesped(){
        int filaselec=tablaHUESPED.getSelectedRow();
        String valor = tablaHUESPED.getValueAt(filaselec, 0).toString();
        
        try {
            PreparedStatement eliminar = (PreparedStatement) conect.prepareStatement("DELETE FROM huesped WHERE nombre='"+valor+"'");
            eliminar.executeUpdate();
            modelo.removeRow(filaselec);
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "No se logro eliminar el registro");
        }
    }

